export function buscarClientePorSKU(sku: string): ClienteData | null {
  const skuLimpio = sku.replace(/\s+/g, "").trim().toLowerCase()

  const resultado = clientesData.find(
    (cliente) =>
      cliente.xPartNum.toLowerCase().includes(skuLimpio) || cliente.partNum.toLowerCase().includes(skuLimpio),
  )

  return resultado || null
}

export function buscarClientesPorSKU(sku: string): ClienteData[] {
  const skuLimpio = sku.replace(/\s+/g, "").trim().toLowerCase()

  if (skuLimpio.length === 0) return []

  return clientesData
    .filter(
      (cliente) =>
        cliente.xPartNum.toLowerCase().includes(skuLimpio) ||
        cliente.partNum.toLowerCase().includes(skuLimpio) ||
        cliente.description.toLowerCase().includes(skuLimpio),
    )
    .slice(0, 10) // Limitar a 10 resultados
}
